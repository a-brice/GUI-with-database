create database if not exists velomax;
use velomax;

drop table if exists adhere;
drop table if exists associe;
drop table if exists compose;
drop table if exists delivrer;
drop table if exists grandeur;
drop table if exists piece;
drop table if exists modele;
drop table if exists fournisseur;
drop table if exists commande;
drop table if exists programme;
drop table if exists boutique;
drop table if exists individu;


create table if not exists individu (no_i varchar(8) primary key not null, 
						nom_i varchar(40) not null, 
						prenom_i varchar(40) not null,
                        adresse_i varchar(200),
                        tel_i int,
                        couriel_i varchar(40));
              
              
create table if not exists boutique (no_b varchar(8) primary key not null, 
						nom_b varchar(40) not null, 
                        adresse_b varchar(200),
                        tel_b int,
                        couriel_b varchar(40),
                        contact_b varchar(20),
                        remise double);


create table if not exists programme (no_programme int primary key not null,
                                    description varchar(200),
                                    cout double not null,
                                    duree_p int,
                                    rabais double);

 
create table if not exists fournisseur (siret double primary key not null, 
										nom_f varchar(20) not null, 
                                        contact varchar(20),
                                        adresse_f varchar(200),
                                        libelle int);


create table if not exists modele (no_m varchar(8) primary key not null,
									nom_m varchar(40),
                                    prix_m double,
                                    categorie varchar(20),
                                    date_debut date,
                                    date_fin date);


create table if not exists piece (no_p varchar(8) primary key not null,
									nom_p varchar(40),
                                    prix double,
									description varchar(400),
                                    date_debut date,
                                    date_fin date);
                                    

create table if not exists delivrer (no_p varchar(8) not null,
									siret double not null,
                                    delai int,
                                    cout double,
                                    num_piece varchar(10),
                                    stock int,   
                                    primary key(siret,no_p),
                                    foreign key (siret) references fournisseur(siret) on delete cascade,
                                    foreign key (no_p) references piece(no_p) on delete cascade);


create table if not exists commande (no_c varchar(8) primary key not null,
									no_client varchar(8) not null,
									date_c date,
                                    date_l date
									);
				
                
create table if not exists compose (no_c varchar(8) not null,
											no_equipement varchar(8) not null,
                                            quantite int,
                                            primary key(no_equipement,no_c));
   


create table if not exists grandeur (label varchar(20) not null,
									no_m varchar(8) not null,
                                    stock int,
                                    primary key(label,no_m),
                                    foreign key(no_m) references modele(no_m) on delete cascade);
                                    
                                    

create table if not exists associe ( no_m varchar(8) not null,
										no_p varchar(8) not null,
                                        grandeur varchar(20),
                                        primary key(no_p,no_m,grandeur),
										foreign key (no_p) references piece(no_p) on delete cascade,
										foreign key (no_m) references modele(no_m) on delete cascade,
                                        foreign key (grandeur) references grandeur(label) on delete cascade);


					
                    
create table if not exists adhere (no_i varchar(8) not null,
									no_programme int not null,
                                    date_debut date,                                 
                                    primary key (no_i,no_programme),
                                    foreign key (no_i) references individu(no_i) on delete cascade,
                                    foreign key (no_programme) references programme(no_programme) on delete cascade);


